#include "libft.h"

char	*ft_strrchr(char *s, int c)
{
	int i;

	i = 0;
	while (s[i] != '\0')
		i++;
	while (i >= 0)
	{
		if (s[i] == (unsigned char)c)
			return (&s[i]);
		i--;
	}
	return (NULL);
}
