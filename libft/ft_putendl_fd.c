#include "libft.h"

void	ft_putendl_fd(char const *s, int fd)
{
	char temp;

	temp = ' ';
	while (*s != '\0')
	{
		temp = *s;
		write(fd, &temp, 1);
		s++;
	}
	write(fd, "\n", 1);
}
